    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int intApplicationMark = jTabbedPane2.getSelectedIndex();
        DecimalFormat df = new DecimalFormat("###.000");

        //Lock the chart. 
        jComboBox2.setEnabled(false);
        jComboBox3.setEnabled(false);
        boolMAFChanged = false;
        boolLDChanged = false;        

        //All Variants
        if (intApplicationMark == -1) {
            return;
        } else if (intApplicationMark == 0) {
            //Initialize Self-define
            simpOne.intTotalSNP = Integer.parseInt(jTextField1.getText().trim());
            simpOne.intBlockNum = Integer.parseInt(jTextField18.getText().trim());
            simpOne.intRR1 = Integer.parseInt(jTextField10.getText().trim());

            simpOne.dblMAFStart = Double.parseDouble(jTextField2.getText().trim());
            simpOne.dblMAFEnd = Double.parseDouble(jTextField16.getText().trim());
            simpOne.dblMAFStep = Double.parseDouble(jTextField17.getText().trim());

            dcbmMAF.removeAllElements();
            for (double i = simpOne.dblMAFStart; i <= simpOne.dblMAFEnd; i += simpOne.dblMAFStep) {
                dcbmMAF.addElement(Double.parseDouble(df.format(i)));
            }

            dcbmLD.removeAllElements();
            dcbmLD.addElement(Double.parseDouble(df.format(0)));

            simpOne.boolSNPIndependenceFlag1 = jRadioButton1.isSelected();
            if (!simpOne.boolSNPIndependenceFlag1) {
                simpOne.dblLDStart = Double.parseDouble(jTextField23.getText().trim());
                simpOne.dblLDEnd = Double.parseDouble(jTextField24.getText().trim());
                simpOne.dblLDStep = Double.parseDouble(jTextField25.getText().trim());

                dcbmLD.removeAllElements();
                for (double j = simpOne.dblLDStart; j <= simpOne.dblLDEnd; j += simpOne.dblLDStep) {
                    dcbmLD.addElement(Double.parseDouble(df.format(j)));
                }
            }
        } else if (intApplicationMark == 1) {
            //Initialize data-plink
            simpOne.strFamilyFile = pedigreeFilePathTextField.getText().trim();
            simpOne.strMapFile = mapFilePathTextField.getText().trim();
            simpOne.strBEDFile = bedFilePathTextField.getText().trim();

            simpOne.intVariant2 = Integer.parseInt(jTextField26.getText().trim());
            simpOne.intRR2 = Integer.parseInt(jTextField27.getText().trim());
            simpOne.boolSNPIndependenceFlag2 = jRadioButton3.isSelected();

            //Set ComboBox. 
            dcbmMAF.removeAllElements();
            dcbmMAF.addElement(Double.parseDouble(df.format(0)));
            dcbmLD.removeAllElements();
            dcbmLD.addElement(Double.parseDouble(df.format(0)));

        } else {
            //Initialize data-vcf
            simpOne.strVCFFile = vcfFilePathTextField.getText().trim();

            simpOne.intVariant3 = Integer.parseInt(jTextField28.getText().trim());
            simpOne.intRR3 = Integer.parseInt(jTextField29.getText().trim());
            simpOne.boolSNPIndependenceFlag3 = jRadioButton5.isSelected();

            //Set ComboBox. 
            dcbmMAF.removeAllElements();
            dcbmMAF.addElement(Double.parseDouble(df.format(0)));
            dcbmLD.removeAllElements();
            dcbmLD.addElement(Double.parseDouble(df.format(0)));
        }

        //Risk Variants
        simpOne.intRiskSNPStart = Integer.parseInt(jTextField3.getText().trim());
        simpOne.intRiskSNPEnd = Integer.parseInt(jTextField14.getText().trim());
        simpOne.intRiskSNPStep = Integer.parseInt(jTextField15.getText().trim());

        simpOne.dblOddsStart = Double.parseDouble(jTextField4.getText().trim());
        simpOne.dblOddsEnd = Double.parseDouble(jTextField5.getText().trim());
        simpOne.dblOddsStep = Double.parseDouble(jTextField6.getText().trim());

        simpOne.dblDiseasePrevalence = Double.parseDouble(jTextField7.getText().trim());
        simpOne.intGeneticModel = jComboBox1.getSelectedIndex();
        
        //Postpone to produce the positions. 
//        String strTemp = jTextField20.getText().trim();
//        if (strTemp == null | "".equals(strTemp)) {
//            jButton5ActionPerformed(null);
//        }
//        strTemp = jTextField20.getText().trim();
//        String[] strElements = strTemp.split(", *| +|; *");
//        simpOne.altRiskSNPPosition = new ArrayList();
//        for (String strElement : strElements) {
//            simpOne.altRiskSNPPosition.add(Integer.parseInt(strElement));
//        }

        //Population & Sample        
        simpOne.intPopulation = Integer.parseInt(jTextField9.getText().trim());
        simpOne.intCase = Integer.parseInt(jTextField12.getText().trim());
        simpOne.intControl = Integer.parseInt(jTextField13.getText().trim());

        //Simulation & Test
        simpOne.intSimulationTimes = 1;
        simpOne.intSamplingTimes = Integer.parseInt(jTextField11.getText().trim());
        simpOne.dblPValueThreshold = Double.parseDouble(jTextField8.getText().trim());

        if (metaAnaComboBox.getSelectedIndex() != 0) {
            int intNumOfStudies = Integer.parseInt(numStudyTextField.getText().trim());
            if (intNumOfStudies <= 0) {
                return;
            }
            int intCase = (int) (simpOne.intPopulation * simpOne.dblDiseasePrevalence / intNumOfStudies);
            if (intCase <= simpOne.intCase) {
                int population = (int) (simpOne.intCase * intNumOfStudies / simpOne.dblDiseasePrevalence);
                simpOne.intPopulation = population;
                jTextField9.setText(Integer.toString(population));
            }
        }
        simpOne.parellelRunNum = Integer.parseInt(parellelRunNumTextField.getText().trim().trim());
        simpOne.intMetaAnalysis = metaAnaComboBox.getSelectedIndex();
        simpOne.intNumOfStudies = Integer.parseInt(numStudyTextField.getText().trim());
        simpOne.boolShuffle = jCheckBox1.isSelected();

        //SKAT
        simpOne.boolSKAT = boolSKAT;
        simpOne.skatOne = skatOne;
        if(boolSKAT)    {
            skatOne.setPhenotype(simpOne.intCase, simpOne.intControl);
            simpOne.intParallel=Integer.parseInt(parellelRunNumTextField.getText().trim().trim());
            simpOne.parellelRunNum=1;
            skatOne.startRServe(simpOne.intParallel);
        }
        //Others
        simpOne.intApplicationFlag = intApplicationMark;
        simpOne.jdOne = this;
        jProgressBar1.setValue(0);

        //Clear power list
        lstSNPPower.clear();
        lstGenePower.clear();
        simulatorOne = new Simulator(simpOne);
        simulatorOne.execute();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        String path;
        JFileChooser fDialog;
        if ((GlobalManager.lastAccessedPath != null) && (GlobalManager.lastAccessedPath.trim().length() > 0)) {
            fDialog = new JFileChooser(GlobalManager.lastAccessedPath);
        } else {
            fDialog = new JFileChooser();
        }
        fDialog.setDialogTitle("Choose a Pedigree File with Genotypes");
        
        int result = fDialog.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            GlobalManager.lastAccessedPath = fDialog.getSelectedFile().getPath();
            GlobalManager.lastAccessedPath = GlobalManager.lastAccessedPath.substring(0, GlobalManager.lastAccessedPath.lastIndexOf(File.separator) + 1);

            try {
                File file = fDialog.getSelectedFile();
                path = file.getCanonicalPath();
                pedigreeFilePathTextField.setText(path);
                if (mapFilePathTextField.getText().trim().trim().length() == 0) {
                    int extPos = path.lastIndexOf(".");
                    String prefixName = "";
                    if (extPos >= 0) {
                        prefixName = path.substring(0, extPos);
                    }
                    File tmpFile = new File(prefixName + ".bim");
                    if (tmpFile.exists()) {
                        mapFilePathTextField.setText(prefixName + ".bim");
                    }
                }
                if (bedFilePathTextField.getText().trim().trim().length() == 0) {
                    int extPos = path.lastIndexOf(".");
                    String prefixName = "";
                    if (extPos >= 0) {
                        prefixName = path.substring(0, extPos);
                    }
                    File tmpFile = new File(prefixName + ".bed");
                    if (tmpFile.exists()) {
                        bedFilePathTextField.setText(prefixName + ".bed");
                    }
                }

            } catch (Exception ex) {
                ErrorManager.getDefault().notify(ex);
            }

        } else {
            // JOptionPane.showMessageDialog(this, FILE_OPEN_CANCELLED, "Message", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton11ActionPerformed

