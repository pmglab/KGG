    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        if (simulatorOne != null && !simulatorOne.isDone()) {
            String infor = "Do you want to stop the running simulation?";
            Object[] options = {"Yes", "No"};
            int response = JOptionPane.showOptionDialog(this, infor, "Message", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE,
                    null, options, options[1]);
            if (response == 0) {
                simulatorOne.cancel(true);
                jProgressBar1.setIndeterminate(false);
                jProgressBar1.setValue(0);
            }
        }
    }//GEN-LAST:event_formWindowClosing

    private void jCheckBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox2ActionPerformed
        // TODO add your handling code here:

        if (jCheckBox2.isSelected()) {
            boolSKAT = true;
            try {
                JOptionPane.showMessageDialog(this, "The speed will decrease dramatically when SKAT added. Please be patient to wait for the response!","Tip Message", JOptionPane.WARNING_MESSAGE);
                skatOne = new SKAT();
            } catch (RserveException ex) {
                if (ex.getMessage().contains("Cannot connect")) {
                    // System.out.println(ex.getMessage() + "\t" + ex.getRequestReturnCode() + "\t" + ex.getRequestErrorDescription());
                    String infor = "Please open your R and type the following commands to allow kgg to use it:\npack=\"Rserve\";\n"
                            + "if (!require(pack,character.only = TRUE))   { install.packages(pack,dep=TRUE,repos=\'http://cran.us.r-project.org\');   if(!require(pack,character.only = TRUE)) stop(\"Package not found\")   }\n"
                            + "library(\"Rserve\");\nRserve(debug = FALSE, port = 6311, args = NULL)\n";
                    FormatShowingDialog dialog = new FormatShowingDialog(this, false, "Rserve connection!", infor);
                    dialog.setLocationRelativeTo(this);
                    dialog.setVisible(true);
                    // java.util.logging.Logger.getLogger(infor);
                };
            }
            if (skatOne == null) {
                jCheckBox2.setSelected(false);
                boolSKAT = false;
            }
        }else{
            boolSKAT=false;
            skatOne=null;
        }


    }//GEN-LAST:event_jCheckBox2ActionPerformed

